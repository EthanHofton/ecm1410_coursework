package cycling;

/**
 * This enum is used to represent the state of the stage.
 * 
 * @author Ethan Hofton
 * @author Jon Tau
 * @version 1.0
 *
 */
public enum StageState {

    /**
	 * Before the stage has concluded its preperation
	 */
    STAGE_PREPERATION,

    /**
	 * Stage is waiting for results to be entered
	 */
    WAITING_FOR_RESULTS;
}
